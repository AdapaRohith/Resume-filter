export const getCandidate = (id) => {
  return mockCandidates.find(c => c.id === parseInt(id))
}

export const getDashboardStats = () => {
  return {
    totalApplications: mockCandidates.length,
    strongMatches: mockCandidates.filter(c => c.category === "Strong").length,
    considered: mockCandidates.filter(c => c.category === "Consider").length,
    rejected: mockCandidates.filter(c => c.category === "Reject").length,
    averageScore: Math.round(mockCandidates.reduce((acc, c) => acc + c.score, 0) / mockCandidates.length)
  }
}

export const getRecruitmentFunnel = () => {
  return {
    applications: mockCandidates.length,
    screened: mockCandidates.length,
    shortlisted: mockCandidates.filter(c => c.emailStatus.shortlisted).length,
    interview: Math.floor(mockCandidates.filter(c => c.emailStatus.shortlisted).length * 0.6),
    hired: 2
  }
}

